void main() {
  int anInteger = 10;
  print('Integer example: $anInteger');
  
  double aDouble = 10.5;
  print('Double example: $aDouble');
  
  String aString = "Hello, Dart!";
  print('String example: $aString');
  
  List<String> aList = ['Apple', 'Banana', 'Cherry'];
  print('List example: $aList');
  
  Map<String, dynamic> aMap = {
    'id': 1,
    'name': 'John Doe',
    'isVerified': true
  };
  print('Map example: $aMap');
  
  print('\nDemonstrating the use of Dart data types:');
  print('A whole number (int): $anInteger');
  print('A floating-point number (double): $aDouble');
  print('A string of text (String): $aString');
  print('A list of strings (List<String>): $aList');
  print('A map with string keys and dynamic values (Map<String, dynamic>): $aMap');
}

